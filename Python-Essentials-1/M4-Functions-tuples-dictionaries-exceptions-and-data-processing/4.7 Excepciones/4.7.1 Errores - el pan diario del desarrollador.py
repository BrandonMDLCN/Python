# Errores en los datos frente a errores en el código

# La primera es cuando te metes en problemas porque tu código, aparentemente correcto, se alimenta con datos incorrectos.
# Por ejemplo, esperas que se ingrese al código un valor entero, pero tu usuario descuidado ingresa algunas letras al azar.

# La segunda parte de lidiar con errores de programación se revela cuando ocurre un comportamiento no deseado del programa 
# debido a errores que se cometieron cuando se estaba escribiendo el código.
# Este tipo de error se denomina comúnmente "bug"