# 4.7.13 Pruebas unitarias - un mayor nivel de codificación

# no se trata solo de probar el software, sino también (y, sobre todo) de cómo se escribe el código.

# las pruebas unitarias asumen que las pruebas son partes inseparables del código y la preparación de los datos de prueba es una parte inseparable de la codificación.


# Esto significa que cuando escribes una función o un conjunto de funciones cooperativas, 
# también estás obligado a crear un conjunto de datos para los cuales el comportamiento de tu código es predecible y conocido.

# debes equipar a tu código con una interfaz que pueda ser utilizada por un entorno de pruebas automatizado. 
# cualquier enmienda realizada al código (incluso la menos significativa) debe ir seguida de la ejecución de todas las pruebas unitarias que acompañan al código fuente.

